    void tablaIngredientes(){
          DefaultTableModel model = (DefaultTableModel)tabla.getModel();
          Object[] row;
            for(int i = 0; i < listaIngredientes.size() ; i++){
                   row = new Object[2];
                   row[0] = listaIngredientes.get(i).getNombre(); 
                   row[1] = listaIngredientes.get(i).getCantidad()+listaIngredientes.get(i).getUnidadPeso();
                   model.addRow(row); 
            }
    }
