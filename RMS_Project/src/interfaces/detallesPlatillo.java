    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if(!(txtNombre.getText().equalsIgnoreCase("") || txtPrecio.getText().equalsIgnoreCase(""))){
           if(!pXML.verificarPlatillo(txtNombre.getText())){
               for(int i = 0; i<listaIngredientes.size();i++){
                  listaIngredientes.get(i).setPlatillo(txtNombre.getText());
                  imXML.agregarIngrediente(listaIngredientes.get(i));
                }
               Platillo p = new Platillo(txtNombre.getText(),Double.parseDouble(txtPrecio.getText()),listaIngredientes.size(),0,comboTipo.getSelectedItem().toString());
               pXML.agregarPlatillo(p);
               JOptionPane.showMessageDialog(null, "El platillo se registro de manera exitosa.");
               volverInicio();
           }
           else{
               JOptionPane.showMessageDialog(null, "El platillo ya se encuentra registrado.");
           }
       }
       else{
           JOptionPane.showMessageDialog(null, "No deje espacios vacios.");
       }
    }//GEN-LAST:event_jButton1ActionPerformed

